import random  # For generating random dice rolls
import time    # For optional pauses to make it feel more realistic

"""*Parse and Validate Input*"""

def parse_input():
    """Ask the user how many dice to roll and validate the input."""
    while True:
        user_input = input("ðŸŽ¯ How many dice would you like to roll (1â€“6)? ")

        if user_input.strip().isdigit():  # check if the input is a number
            num_dice = int(user_input)

            if 1 <= num_dice <= 6:
                return num_dice
            else:
                print("âš ï¸ Please enter a number between 1 and 6.")
        else:
            print("âš ï¸ Invalid input. Please enter a number.")

"""*Simulate Rolling Dice*"""

def roll_dice(num_dice):
    """Simulate rolling 'num_dice' dice."""
    results = []
    for _ in range(num_dice):
        results.append(random.randint(1, 6))
    return results

