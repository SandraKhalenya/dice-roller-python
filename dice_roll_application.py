"""*Generate Ascii Diagram*"""

def generate_dice_faces_diagram(dice_values):
    """Generate an ASCII diagram of dice faces side by side."""
    dice_faces = [DICE_ART[value] for value in dice_values]
    dice_faces_rows = [" ".join(row[i] for row in dice_faces) for i in range(5)]
    return "\n".join(dice_faces_rows)

